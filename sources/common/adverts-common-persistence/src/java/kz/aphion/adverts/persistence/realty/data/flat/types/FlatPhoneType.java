/**
 * Телефон и его типы
 * 
 * @author artem.demidovich
 *
 */
public enum FlatPhoneType {
	
	/**
	 * Не указано
	 */
	UNDEFINED,
	
	/**
	 * Отдельный
	 */
	SEPARETED,
	
	/**
	 * Блокиратор
	 */
	LOCKING,
	
	/**
	 * Есть возможность подключения
	 */
	ABILITY_TO_CONNECT,
	
	/**
	 * Нет телефона
	 */
	NO_PHONE,
	
}
